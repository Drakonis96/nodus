// Real local-text integration check. It consumes transcripts emitted by
// verify-study-whisper.mjs and sends them through Nodus's complete improvement
// pipeline using a live Ollama model (no mocked provider and no external API).
import assert from 'node:assert/strict';
import { execFileSync } from 'node:child_process';
import fs from 'node:fs';
import { mkdtemp, readFile, rm } from 'node:fs/promises';
import { createRequire } from 'node:module';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const require = createRequire(import.meta.url);
const reportPath = process.env.NODUS_WHISPER_REPORT;
const model = process.env.NODUS_IMPROVE_MODEL || 'qwen2.5:1.5b';
if (!reportPath) throw new Error('Set NODUS_WHISPER_REPORT to a report generated by verify-study-whisper.mjs.');

if (!process.argv.includes('--electron-study-improve-local')) {
  execFileSync(path.join(repoRoot, 'node_modules/.bin/electron'), [path.join(repoRoot, 'scripts/verify-study-improve-local.mjs'), '--electron-study-improve-local'], {
    cwd: repoRoot,
    env: { ...process.env, ELECTRON_RUN_AS_NODE: '1' },
    stdio: 'inherit',
  });
  process.exit(0);
}

const input = JSON.parse(await readFile(path.resolve(reportPath), 'utf8'));
assert.equal(input.backend, 'renderer-wasm', 'the input must come from the real renderer Whisper verifier');
assert.ok(Array.isArray(input.report) && input.report.length > 0, 'the Whisper report contains transcripts');
const transcripts = [...new Map(input.report.map((entry) => [entry.fixture, entry])).values()];
const root = await mkdtemp(path.join(os.tmpdir(), 'nodus-study-improve-local-'));
installRuntimeHooks(root);
try {
  const org = require(path.join(repoRoot, 'electron/db/studyOrgRepo.ts'));
  const settingsRepo = require(path.join(repoRoot, 'electron/db/settingsRepo.ts'));
  const styles = require(path.join(repoRoot, 'electron/db/studyStylesRepo.ts'));
  const usage = require(path.join(repoRoot, 'electron/db/studyAiUsageRepo.ts'));
  const improve = require(path.join(repoRoot, 'electron/ai/studyImprove.ts'));
  const { closeDb } = require(path.join(repoRoot, 'electron/db/database.ts'));

  settingsRepo.updateSettings({
    studyAiEnabled: true,
    studyAiPrivacyMode: 'local',
    studyAiLocalOnly: true,
    studyAiRetryCount: 0,
    studyAiMaxInputChars: 120_000,
    studyAiMaxOutputTokens: 1_200,
    improveModel: { provider: 'ollama', model },
  });

  const results = [];
  for (const transcript of transcripts) {
    assert.ok(transcript.text.trim().split(/\s+/u).length >= 12, `${transcript.fixture} has a substantive real transcript`);
    const document = org.createStudyDocument({
      title: `Whisper · ${transcript.fixture}`,
      kind: 'transcripcion',
      contentMarkdown: transcript.text,
    });
    let streamed = '';
    const started = performance.now();
    const improved = await improve.improveStudyText({
      documentId: document.id,
      text: transcript.text,
      styleId: 'builtin:proofread',
      scope: 'document',
      level: 'minimal',
      length: 'similar',
      mode: 'preserve',
      variables: { language: 'en', documentType: 'transcript' },
      model: { provider: 'ollama', model },
    }, (delta) => { streamed += delta; });
    const originalWords = transcript.text.trim().split(/\s+/u).length;
    const improvedWords = improved.text.trim().split(/\s+/u).length;
    assert.equal(improved.modelProvider, 'ollama');
    assert.equal(improved.modelName, model);
    assert.ok(streamed.length > 0, 'the real provider emits visible streaming deltas');
    assert.ok(improvedWords >= Math.floor(originalWords * 0.65), 'proofreading never collapses the transcript');
    assert.ok(improvedWords <= Math.ceil(originalWords * 1.5), 'proofreading never expands the transcript excessively');
    assert.notEqual(improved.originalHash, improved.resultHash, 'the local model performs a real correction');
    assert.ok(transcript.matched.filter((term) => improved.text.toLocaleLowerCase().includes(term)).length >= 2, 'core detected concepts survive improvement');
    if (transcript.text.includes('50%')) assert.ok(improved.text.includes('50%'), 'protected numeric evidence survives improvement');
    const log = styles.listStudyImprovementLog(document.id).find((entry) => entry.id === improved.logId);
    assert.ok(log, 'the real improvement records auditable provenance');
    results.push({
      fixture: transcript.fixture,
      whisperModel: transcript.model,
      improveModel: model,
      originalWords,
      improvedWords,
      protectedSpans: improved.protectedSpanCount,
      warnings: improved.warnings,
      durationMs: Math.round(performance.now() - started),
      original: transcript.text,
      improved: improved.text,
    });
  }

  const summary = usage.getStudyAiUsageSummary();
  assert.equal(summary.successfulCalls, results.length, 'every real local improvement is accounted for');
  assert.equal(summary.failedCalls, 0);
  assert.equal(summary.knownCostUsd, 0, 'local inference never invents a monetary cost');
  console.log(JSON.stringify({ backend: 'ollama-local', privacy: 'local', results, usage: summary }, null, 2));
  console.log('Real local Study text-improvement verification passed.');
  closeDb();
} finally {
  await rm(root, { recursive: true, force: true });
}

function installRuntimeHooks(userDataPath) {
  const ts = require('typescript');
  const Module = require('node:module');
  const originalResolveFilename = Module._resolveFilename;
  const originalLoad = Module._load;
  const electronStub = {
    app: { getPath: () => userDataPath, getVersion: () => '0.0.0-real-local-test', getAppPath: () => repoRoot, isPackaged: false },
    safeStorage: { isEncryptionAvailable: () => false, encryptString: (value) => Buffer.from(String(value)), decryptString: (value) => Buffer.from(value).toString() },
    dialog: { showMessageBoxSync: () => 0 }, shell: {}, BrowserWindow: class {},
  };
  Module._resolveFilename = function (request, parent, isMain, options) {
    if (request.startsWith('@shared/')) return path.join(repoRoot, `${request.replace('@shared/', 'shared/')}.ts`);
    return originalResolveFilename.call(this, request, parent, isMain, options);
  };
  Module._load = function (request, parent, isMain) {
    if (request === 'electron') return electronStub;
    return originalLoad.call(this, request, parent, isMain);
  };
  require.extensions['.ts'] = function (module, filename) {
    const output = ts.transpileModule(fs.readFileSync(filename, 'utf8'), { fileName: filename, compilerOptions: { target: ts.ScriptTarget.ES2022, module: ts.ModuleKind.CommonJS, moduleResolution: ts.ModuleResolutionKind.NodeJs, esModuleInterop: true, jsx: ts.JsxEmit.ReactJSX, resolveJsonModule: true, skipLibCheck: true } }).outputText;
    module._compile(output, filename);
  };
}
