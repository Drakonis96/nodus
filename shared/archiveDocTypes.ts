/**
 * Document-type taxonomy for evidence-archive items, with an optional per-type
 * metadata form. This classifies PRIMARY SOURCES and heritage items (a birth record,
 * a diary, a photograph, a notarial deed, a gravestone, a castle, a postcard…) —
 * academic/bibliographic material is not an archive item; it belongs in the library
 * via Zotero. Pure and dependency-free so both processes share it.
 *
 * The taxonomy is BILINGUAL in-data (`label` ES + `labelEn`) so ~190 specialised
 * heritage terms travel with the type instead of the i18n table. Each type also
 * carries `keywords` (synonyms, for the semantic search) and `facets` (the heritage
 * dimensions the filter bar exposes). The compact source rows live in
 * `RAW_DOC_TYPES` (generated by scripts/gen-archive-doc-types.mjs) and expand into
 * `ARCHIVE_DOC_TYPES`; legacy types keep their original id + bespoke form.
 *
 * Dates are free-text fields on purpose: genealogical dates are uncertain
 * ("c. 1850", "antes de 1880") and a date picker would fight that.
 */

